CREATE TABLE
IF NOT EXISTS
functions_1 (
    address BIGINT UNSIGNED UNIQUE NOT NULL,
    name TEXT NOT NULL,
    function_type INTEGER UNSIGNED NOT NULL, 
    name_md5 CHAR(32) UNIQUE,
    PRIMARY KEY (address) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
basic_blocks_1 (
    id INTEGER NOT NULL,
    parent_function BIGINT UNSIGNED,
    address BIGINT UNSIGNED NOT NULL,
    PRIMARY KEY(id, parent_function),
    FOREIGN KEY (parent_function) REFERENCES functions_1(address) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
instructions_1 (
    address BIGINT UNSIGNED NOT NULL,
    basic_block_id INTEGER NOT NULL,
    mnemonic VARCHAR(32),
    sequence INT UNSIGNED NOT NULL,
    data BLOB NOT NULL,
    PRIMARY KEY(address, basic_block_id),
    FOREIGN KEY (basic_block_id) REFERENCES basic_blocks_1(id) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
callgraph_1 (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    src BIGINT UNSIGNED NOT NULL, 
    src_basic_block_id INTEGER,
    src_address BIGINT UNSIGNED NOT NULL, 
    dst BIGINT UNSIGNED NOT NULL, 
    FOREIGN KEY (src) REFERENCES functions_1(address), 
    FOREIGN KEY (src_basic_block_id) REFERENCES basic_blocks_1(id), 
    FOREIGN KEY (src_address) REFERENCES instructions_1(address),
    FOREIGN KEY (dst) REFERENCES functions_1(address) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
control_flow_graph_1 (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    parent_function BIGINT UNSIGNED NOT NULL,
    src INTEGER,
    dst INTEGER,
    kind INTEGER, 
    FOREIGN KEY (src) REFERENCES basic_blocks_1(id),
    FOREIGN KEY (dst) REFERENCES basic_blocks_1(id),
    FOREIGN KEY (parent_function) REFERENCES functions_1(address) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
operand_strings_1 (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    str TEXT )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
expression_tree_1 (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    expr_type INTEGER NOT NULL, 
    symbol VARCHAR(256), 
    immediate BIGINT SIGNED, 
    position INTEGER, 
    parent_id INTEGER CHECK(id > parent_id),
    FOREIGN KEY (parent_id) REFERENCES expression_tree_1(id)
    )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
operand_tuples_1 (
    address BIGINT UNSIGNED NOT NULL,
    operand_id INTEGER,
    position INTEGER,
    FOREIGN KEY (operand_id) REFERENCES operand_strings_1(id),
    FOREIGN KEY (address) REFERENCES instructions_1(address) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
expression_substitutions_1 (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    address BIGINT UNSIGNED NOT NULL,
    operand_id INTEGER,
    expr_id INTEGER,
    replacement TEXT,
    FOREIGN KEY (address) REFERENCES instructions_1(address),
    FOREIGN KEY (operand_id) REFERENCES operand_strings_1(id),
    FOREIGN KEY (expr_id) REFERENCES expression_tree_1(id) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
operand_expressions_1 (
    operand_id INTEGER,
    expr_id INTEGER,
    FOREIGN KEY (operand_id) REFERENCES operand_strings_1(id),
    FOREIGN KEY (expr_id) REFERENCES expression_tree_1(id) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
address_references_1 (
    address BIGINT UNSIGNED NOT NULL,
    target BIGINT UNSIGNED NOT NULL,
    kind INTEGER  )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
data_1 (
    address BIGINT UNSIGNED UNIQUE NOT NULL,
    name VARCHAR(64),
    length BIGINT UNSIGNED NOT NULL,
    data BLOB NOT NULL,
    is_string BOOL,
    PRIMARY KEY(address) )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
sections_1 (
    name VARCHAR(256),
    base BIGINT UNSIGNED NOT NULL,
    start_address BIGINT UNSIGNED NOT NULL,
    end_address BIGINT UNSIGNED NOT NULL,
    length BIGINT UNSIGNED NOT NULL,
    data LONGBLOB )
ENGINE=INNODB;
CREATE TABLE
IF NOT EXISTS
address_comments_1 (
    address BIGINT UNSIGNED UNIQUE NOT NULL,
    comment TEXT,
    PRIMARY KEY(address) )
ENGINE=INNODB;
